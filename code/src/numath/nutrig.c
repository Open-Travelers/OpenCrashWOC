void NuTrigInit()
{
	f32 inc = 0.00009587f;
	for (s32 i = 0; i < NU_TRIG_TABLE_SIZE; i++)
	{
		NuTrigTable[i] = (f32)sin((f64)(i * inc));
		//printf("table: %d\n", NuTrigTable[i]);
	}
}

angle xy(fxi x, fxi y)
{
	return x <= y ? ang[(x << 9) / y] : (PIOV2 - ang[(y << 9) / x]);
}

angle NuAtani(fxi x, fxi y)
{
	angle ret;
	if (x == 0)
	{
		ret = (angle)((u32)y >> 0x10) & PI;
	}
	else
	{
		if (y == 0)
		{
			ret = PIOV2;
			if (x < 0)
			{
				ret = PI3OV2;
			}
		}
		else
		{
			if (x < 0)
			{
				if (y < 0)
				{
					ret = xy(-x, -y);
					ret += PI;
				}
				else
				{
					ret = xy(-x, y);
					ret = -ret;
				}
			}
			else
			{
				if (y < 0)
				{
					ret = xy(x, -y);
					ret = PI - ret;
				}
				else
				{
					ret = xy(x, y);
				}
			}
		}
	}
	return ret;
}

f32 NuAtan2(f32 x, f32 y)
{
	return (f32)atan2(x, y);
}

angle fxyd(f32 x, f32 y)
{
	angle ret;
	if (y < x)
	{
		ret = PIOV2 - ang[(s32)((y * 512.0) / x)];
	}
	else
	{
		ret = ang[(s32)((x * 512.0) / y)];
	}
	return ret;
}

angle NuAtan2D(f32 x, f32 y)
{
	angle ret;
	if (x == 0.0)
	{
		ret = 0;
		if (y < 0.0)
		{
			ret = PI;
		}
	}
	else
	{
		if (y == 0.00) {
			ret = PIOV2;
			if (x < 0.0)
			{
				ret = PI3OV2;
			}
		}
		else {
			if (x >= 0.0)
			{
				if (y < 0.0)
				{
					ret = fxyd(x, -y);
					ret = PI - ret;
				}
				else
				{
					ret = fxyd(x, y);
				}
			}
			else
			{
				if (y < 0.0)
				{
					ret = fxyd(-x, -y);
					ret += PI;
				}
				else
				{
					ret = fxyd(-x, y);
					ret = -ret;
				}
			}
		}
	}
	return ret;
}

angle NuAngleSub(angle a, angle b)
{
	return a - b;
}
